/**
 * add - function that adds two numbers
 *
 * @a: int
 * @b: int
 * Return: int
 */
int add(int a, int b)
	{
		return (a + b);
	}

/**
 * sub - function that subtracts two numbers
 *
 * @a: int
 * @b: int
 * Return: int
 */
int sub(int a, int b)
	{
		return (a - b);
	}

/**
 * mul - function that products two numbers
 *
 * @a: int
 * @b: int
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that divides two numbers
 *
 * @a: int
 * @b: int
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that modulars two numbers
 *
 * @a: int
 * @b: int
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
